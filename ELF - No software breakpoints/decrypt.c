#include <stdio.h>

int main ()
{
	int checksum = 0xac77e166;
	int key[] = {0x1E, 0xCD, 0x2A, 0xD5, 0x34, 0x87, 0xFC, 0x78, 0x64, 0x35, 0x9D, 0xEC, 0xDE, 0x15, 0xAC, 0x97, 0x99, 0xAF, 0x96, 0xDA, 0x79, 0x26, 0x4F, 0x32, 0xE0};
	int ret[25];
	for (int i = 24; i != -1; i--)
	{
		checksum = (checksum >> 1) | (checksum << (32 - 1));
		int res = checksum ^ key[i];
		ret[i] = res;
	}
	for (int i = 0; i < 25; i++)
		printf("%c", ret[i]);
	printf("\n");
}
